SELECT ITEM.ITEM_CD
	 , ITEM.ITEM_NM
	 , ITEM.ITEM_ATTR01_CD
	 , ITEM.ITEM_ATTR02_CD
	 , ITEM.ITEM_ATTR03_CD
	 , ITEM.ITEM_ATTR04_CD
	 , ITEM.ITEM_ATTR01_NM
	 , ITEM.ITEM_ATTR02_NM
	 , ITEM.ITEM_ATTR03_NM
	 , ITEM.ITEM_ATTR04_NM
FROM VIEW_I002040 ITEM
WHERE 1=1
  AND ITEM.ITEM_ATTR01_CD IN (:VS_CB_BIZ_CD)
  AND ITEM.ITEM_ATTR02_CD IN (:VS_CB_LINE_CD)
  AND ITEM.ITEM_ATTR03_CD IN (:VS_CB_BRAND_CD)
  AND ITEM.ITEM_TYPE_CD = 'FERT'
  AND ITEM.USE_YN 		= 'Y'
ORDER BY ITEM.ITEM_CD


