    , T1.QTY1
    , T1.QTY2
FROM (
        SELECT T1.USER_CD
            , SUM(T1.DP_QTY) QTY1
            , SUM(ISNULL(T3.DP_QTY,0)) QTY2
        FROM M4S_O201001 T1
            JOIN (
                SELECT DP_VRSN_ID
                     , DP_COPY_VRSN_ID
                FROM M4S_O201010
                WHERE DP_VRSN_ID = 'SP_2021W41.02'
            ) T2
            ON T1.DP_VRSN_ID = T2.DP_VRSN_ID
           LEFT OUTER JOIN M4S_O201001 T3
            ON 1=1
            AND T2.DP_COPY_VRSN_ID = T3.DP_VRSN_ID
            AND T1.USER_CD = T3.USER_CD
        WHERE T1.PROJECT_CD = 'ENT001'
        GROUP BY T1.USER_CD
    ) T1
, M4S_I002020 T2
WHERE T1.USER_CD = T2.USER_CD

